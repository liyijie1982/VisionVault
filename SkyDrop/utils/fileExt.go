func GetFileType(ext string) string {
	ext = strings.ToLower(ext)
	if len(ext) == 0 {
		return "OTHER"
	}

	if value, exists := G_SuffixMap[ext]; exists {
		return value
	} else {
		return "OTHER"
	}
}
